import time
from pathlib import Path


def build_markdown_report(analysis, comparison, diagnosis, in_dir, target_cls):
    lines = []
    lines.append("# Audio Refinement Lab — Audit Report")
    lines.append("")
    lines.append(f"**Generated:** {time.strftime('%Y-%m-%d %H:%M:%S UTC', time.gmtime())}")
    lines.append(f"**Directory:** `{in_dir}`")
    if target_cls:
        lines.append(f"**Target Class:** `{target_cls}`")
    lines.append("")

    if analysis:
        stats = analysis.get("stats", {})
        lines.append("## Generated Feature Summary")
        lines.append("")
        lines.append("| Feature | Mean | Std | Min | Max |")
        lines.append("|---------|------|-----|-----|-----|")
        for key in ["spectral_centroid", "low_band_energy", "mid_band_energy",
                     "high_band_energy", "transient_count", "amplitude_peaks",
                     "decay_length_ms", "attack_ms", "rms", "zero_crossing_rate"]:
            if key in stats:
                s = stats[key]
                unit = " Hz" if key == "spectral_centroid" else " ms" if "ms" in key else ""
                fmt_v = "{:.2f}" if "energy" in key or key in ("rms", "zero_crossing_rate") else "{:.1f}"
                lines.append(f"| {key.replace('_', ' ')} | {fmt_v.format(s['mean'])}{unit} | {fmt_v.format(s['std'])}{unit} | {fmt_v.format(s['min'])}{unit} | {fmt_v.format(s['max'])}{unit} |")
        lines.append("")
        lines.append(f"**Files analyzed:** {analysis.get('num_files', 0)}")
        lines.append("")

    if comparison:
        distances = comparison.get("distances", {})
        nearest = comparison.get("nearest_class")
        td = comparison.get("target_distance")
        lines.append("## Reference Comparison")
        lines.append("")
        lines.append(f"**Nearest reference class:** `{nearest}`")
        if td is not None:
            lines.append(f"**Distance to target `{comparison.get('target_class')}`:** {td:.4f}")
            status = "PASS" if nearest == comparison.get("target_class") else "FAIL"
            lines.append(f"**Status:** {status}")
        lines.append("")
        lines.append("| Reference Class | Distance |")
        lines.append("|----------------|----------|")
        for rc in sorted(distances.keys(), key=lambda c: distances[c]):
            d = distances[rc]
            marker = " ← TARGET" if rc == comparison.get("target_class") else (" ← NEAREST" if rc == nearest else "")
            lines.append(f"| {rc} | {d:.4f}{marker} |")
        lines.append("")

    if diagnosis:
        diags = diagnosis.get("diagnoses", [])
        zs = diagnosis.get("z_scores", {})
        lines.append("## Diagnosis")
        lines.append("")
        if not diags:
            lines.append("**✓ No issues detected — all features within ±2σ of reference.**")
        else:
            lines.append("| # | Issue | Severity | Feature | Detail |")
            lines.append("|---|-------|----------|---------|--------|")
            for i, d in enumerate(diags, 1):
                lines.append(f"| {i} | {d['issue']} | {d['severity']:.0%} | `{d['feature']}` | {d['detail']} |")
        lines.append("")

        if zs:
            lines.append(f"### Z-Scores (Generated vs Reference `{diagnosis.get('target_class', '?')}`)")
            lines.append("")
            lines.append("| Feature | Generated | Ref Mean | Ref Std | Z-Score | Verdict |")
            lines.append("|---------|-----------|----------|---------|---------|---------|")
            for k, zd in zs.items():
                gv = zd.get("generated", 0)
                rm = zd.get("ref_mean", 0)
                rs = zd.get("ref_std", 0)
                z = zd.get("z_score", 0)
                if abs(z) < 1:
                    verdict = "✓ Good"
                elif abs(z) < 2:
                    verdict = "△ Mild"
                elif abs(z) < 3:
                    verdict = "▲ Deviant"
                else:
                    verdict = "✗ Bad"
                unit = " Hz" if k == "spectral_centroid" else " ms" if "ms" in k else ""
                lines.append(f"| {k.replace('_', ' ')} | {gv:.2f}{unit} | {rm:.2f}{unit} | {rs:.2f}{unit} | {z:+.2f} | {verdict} |")
            lines.append("")

    lines.append("---")
    lines.append("*Generated by cShot Audio Refinement Lab*")
    return lines


